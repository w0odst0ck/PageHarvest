"""
选品分析输出模板 (Selection Schema)
====================================

所有平台的选品分析器输出格式统一。
本文件定义标准输出规范，各平台 picker 应参照此模板实现。

═══════════════════════════════════════════════════════════════
一、输出文件结构
═══════════════════════════════════════════════════════════════

{output_dir}/{品类}/
├── 00-选品推荐合集.csv      ← 所有策略合并（共 10 条）
├── 🔥 必上.csv             ← 必上商品（5 条）
├── 👍 推荐.csv             ← 推荐商品（2 条）
├── 💡 暗马.csv             ← 暗马商品（2 条）
└── 📌 关注.csv             ← 关注商品（1 条）

{output_dir}/选品分析_{品类}.txt   ← 可读分析报告


═══════════════════════════════════════════════════════════════
二、各策略输出数量
═══════════════════════════════════════════════════════════════

每次输出固定 10 条，必上占一半，剩余按推荐/暗马/关注分配。

策略    数量    说明
─────  ─────  ────────────────────
🔥 必上   5     高销量+自营+高好评，核心必上
👍 推荐   2     中高销量或高好评，值得推荐
💡 暗马   2     中等销量+高好评，有潜力的黑马
📌 关注   1     有特色标签或价格优势，保持关注
合计    10


═══════════════════════════════════════════════════════════════
三、CSV 字段定义
═══════════════════════════════════════════════════════════════

合集文件字段（按顺序）:

  策略,排名,品牌,标题,价格,[平台扩展列],链接

策略文件字段（不含"策略"列）:

  排名,品牌,标题,价格,[平台扩展列],链接

平台扩展列:

  ZKH: 合集中→ 型号,行家精选   策略中→ 型号,行家精选,页码,图片数
  JD:  合集中→ 销量,好评率,自营   策略中→ 销量,好评率,自营

字段说明:
  - 策略:  🔥 必上 / 👍 推荐 / 💡 暗马 / 📌 关注
  - 排名:  在搜索页结果中的排序位置（1-based）
  - 品牌:  商品品牌（搜索页无法提取时为空）
  - 标题:  商品标题
  - 价格:  纯数字（无 ¥ 前缀），保留两位小数
  - 链接:  商品详情页 URL

"""

# ═══════════════════════════════════════════════════════════════
#  策略输出数量配置
# ═══════════════════════════════════════════════════════════════

STRATEGY_LIMITS = {
    "🔥 必上": 5,
    "👍 推荐": 2,
    "💡 暗马": 2,
    "📌 关注": 1,
}

STRATEGY_ORDER = ["🔥 必上", "👍 推荐", "💡 暗马", "📌 关注"]


# ═══════════════════════════════════════════════════════════════
#  所有平台共有的列（按顺序）
# ═══════════════════════════════════════════════════════════════

COMMON_COLUMNS_COLLECTION = ["策略", "排名", "品牌", "标题", "价格"]
COMMON_COLUMNS_STRATEGY   = ["排名", "品牌", "标题", "价格"]


# ═══════════════════════════════════════════════════════════════
#  平台扩展列
# ═══════════════════════════════════════════════════════════════

PLATFORM_COLUMNS = {
    "zkh": {
        "collection": ["型号", "行家精选"],
        "strategy":   ["型号", "行家精选", "页码", "图片数"],
    },
    "jd": {
        "collection": ["销量", "好评率", "自营"],
        "strategy":   ["销量", "好评率", "自营"],
    },
    "1688": {
        "collection": ["年销", "复购率", "店铺"],
        "strategy":   ["年销", "复购率", "店铺"],
    },
}


def get_collection_fields(platform: str) -> list[str]:
    """获取合集的完整字段列表"""
    return COMMON_COLUMNS_COLLECTION + PLATFORM_COLUMNS.get(platform, {}).get("collection", []) + ["链接"]


def get_strategy_fields(platform: str) -> list[str]:
    """获取策略文件的完整字段列表"""
    return COMMON_COLUMNS_STRATEGY + PLATFORM_COLUMNS.get(platform, {}).get("strategy", []) + ["链接"]
